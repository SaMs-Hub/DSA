#include <bits/stdc++.h>
using namespace std;

int main(){
    int n, i, j;
    cin >> n;
    i = 1;
    while (i <= n){
        j = 1;
        while (j <= n){
            cout << '*';
            j += 1;
        }
        cout << endl;
        i += 1;
    }
}


